N = input()  # 방 번호 입력
dp = [0] * 10  # 0부터 9까지의 숫자 등장 빈도를 저장하는 리스트

# 방 번호의 각 숫자에 대해 빈도를 계산
for num in N:
    dp[int(num)] += 1

# 6과 9는 서로 대체 가능하므로, 두 숫자의 빈도를 합쳐서 2로 나눈 값을 반영
dp[6] = (dp[6] + dp[9] + 1) // 2  # +1을 해주는 이유는 홀수일 경우를 고려하기 위함
dp[9] = dp[6]  # dp[9]도 동일하게 맞춤

# 최대값을 출력
print(max(dp))
